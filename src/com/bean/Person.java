package com.bean;

public class Person {
private int pid;
private String name;
private int age;
private String address;
private Room room;
public int getPid() {
	return pid;
}
public void setPid(int pid) {
	this.pid = pid;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public Room getRoom() {
	return room;
}
public void setRoom(Room room) {
	this.room = room;
}
}
